// left-shift

'use strict';

(function () {
  // ----------------------------------------------------------------------------------------------------
  // `<<` 表示左位移,
  // 符号位不会受影响,
  // 右边的空位用 0 补齐,

  // 2 = 0000 0000 0000 0000 0000 0000 0000 0010
  // << 5
  // --------------------------------------------
  //     0000 0000 0000 0000 0000 0000 0100 0000

  console.assert(2 << 5 === 64);

  // -2 = 1111 1111 1111 1111 1111 1111 1111 1110
  // << 5
  // --------------------------------------------
  //      1111 1111 1111 1111 1111 1111 1100 0000

  console.assert(-2 << 5 === -64);
})();
