// unsigned-right-shift

'use strict';

(function () {
  // ----------------------------------------------------------------------------------------------------
  // `>>>` 表示无符号的右位移,
  // 跟 `>>` 的区别在于, 它会移动符号位, 且左边的空位始终用 0 补齐

  // 64 = 0000 0000 0000 0000 0000 0000 0100 0000
  // >>> 5
  // --------------------------------------------
  //      0000 0000 0000 0000 0000 0000 0000 0010

  console.assert(64 >>> 5 === 2);

  // -64 = 1111 1111 1111 1111 1111 1111 1100 0000
  // >>> 5
  // --------------------------------------------
  //       0000 0111 1111 1111 1111 1111 1111 1110

  console.assert(-64 >>> 5 === 0b0000_0111_1111_1111_1111_1111_1111_1110);
})();
