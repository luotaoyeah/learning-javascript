// stack-methods

'use strict';

(function () {
  // ----------------------------------------------------------------------------------------------------
  // `Array.push()` 方法,
  // 添加多个元素到数组末尾, 返回新的 `length`

  console.assert([1, 2, 3].push(4, 5) === 5);
})();

(function () {
  // ----------------------------------------------------------------------------------------------------
  // `Array.pop()` 方法,
  // 移除并返回最后的元素

  console.assert([1, 2, 3].pop() === 3);
})();
