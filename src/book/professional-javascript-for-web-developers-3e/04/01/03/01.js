// argument-passing

'use strict';

(function () {
  // ----------------------------------------------------------------------------------------------------
  // 函数参数是值传递

  var x = 1;

  (function (_x) {
    _x = 2;
  })(x);

  console.assert(x === 1);
})();

(function () {
  // ----------------------------------------------------------------------------------------------------

  var obj = { x: 1 };

  (function (_obj) {
    _obj = { x: 2 };
  })(obj);

  console.assert(obj.x === 1);
})();

(function () {
  // ----------------------------------------------------------------------------------------------------

  var obj = { x: 1 };

  (function (_obj) {
    _obj.x = 2;
  })(obj);

  console.assert(obj.x === 2);
})();
