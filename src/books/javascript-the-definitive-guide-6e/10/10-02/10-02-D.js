/*
 * String.prototype.replace()
 */

(function() {
  /*
   * 如果 replace() 的第二个参数包含 "$n" ，其中 n 表示一个数字，
   * 则它会被替换为：第一个参数中对应 n 的分组的匹配结果；
   */
  console.log("\n-------------------------------------------------- 01");

  /* [0]-[1]-[2] */
  console.log("(0)-(1)-(2)".replace(/\((\d+)\)/g, "[$1]"));
})();

(function() {
  console.log("\n-------------------------------------------------- 02");

  /* a-a b-b c-c */
  console.log("a9a b7b c5c".replace(/([a-z])\d\1/g, "$1-$1"));
})();
